namespace VisitTracker;

/// <summary>
/// EExternalLinkType is an enumeration that defines the different types of external links that can be used in the application.
/// Each type corresponds to a specific action or page that the user can navigate to when clicking on the link.
/// </summary>
public enum EExternalLinkType
{
    PROFILE,
    LEAVE,
    AVAILABILITY,
    CONTACTS,
    BOOKING_PREFERENCES,
    TRAININGS,
    LOG_REQUEST,
    SUBMIT_FORMS,
    SUBMIT_OBA_FORMS,
}